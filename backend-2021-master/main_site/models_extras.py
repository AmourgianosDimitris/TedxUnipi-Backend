def validate_year(value):
    if value not in range(2015, datetime.date.today().year + 1):
        raise ValidationError('error: year out of range!')


def member_slug(value):
    c = '^[0-9]{4}-[0-9]{4}$'
    if not re.match(c, value):
        raise ValidationError('error: Wrong Format! The format must look like the default!')

    years = value.split('-')
    if int(years[0]) >= int(years[1]):
        raise ValidationError('error: The starting year must be before the endind year!')


def event_slug(value):
    pre_event = '^pre-[0-9]{4}$'
    main_event = '^[0-9]{4}$'

    if not re.match(pre_event, value) and not re.match(main_event, value):
        raise ValidationError('error: The format must be "2020" or "pre-2020"!')


def default_year():
    current_year = datetime.date.today().year
    sept = datetime.date(current_year, 9, 1)
    jan = datetime.date(current_year + 1, 1, 1)
    current_date = datetime.date.today()
    # current_date = datetime.date(2021, 11, 25)

    if current_date >= sept and current_date <= jan:
        return str(current_year) + "-" + str(current_year + 1)
    else:
        return str(current_year - 1) + "-" + str(current_year)
